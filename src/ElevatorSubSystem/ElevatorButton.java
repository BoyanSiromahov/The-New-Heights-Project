package ElevatorSubSystem;

/**
 * This enumeration class is used to keep track of the Floor Button interaction with
 * the User inside the elevator. It enables processing of floor selection.
 * There are only two states for the buttons: ON indicates the floor has been
 * selected OFF indicates the floor has not been selected
 * 
 * @author Muneeb Nasir
 */
public enum ElevatorButton {
	ON, OFF
}
