package ElevatorSubSystem;
/**
 * This Enum class is used to track Elevator Movement. The indicator can be used to check the arrival and signal the
 * arrival of the elevator to the floor.
 * REACHED_FLOOR indicates the elevator has reached the floor
 * NOT_REACHED_FLOOR indicates the elevator has not reached the floor
 * @author Muneeb Nasir
 */
public enum ArrivalSensor {
    REACHED_FLOOR,
    NOT_REACHED_FLOOR
}
